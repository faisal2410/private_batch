// The box model is a fundamental concept in CSS that describes how HTML elements are represented as rectangular boxes. The box model consists of four parts: the content area, padding, border, and margin.

// The content area is the space inside the element where the actual content is displayed. The padding is the space between the content area and the element's border. The border is a line that surrounds the padding and content area, and the margin is the space between the border and other elements on the page.

// Here's an example of how the box model works in CSS:


// .box {
//   width: 200px;
//   height: 100px;
//   padding: 20px;
//   border: 2px solid black;
//   margin: 10px;
// }
// In this example, we have a CSS rule that targets an element with the class name "box". We have set the width and height of the box to 200 pixels and 100 pixels, respectively. We have also added 20 pixels of padding on all sides of the box, and a 2-pixel-wide solid black border.

// Finally, we have added 10 pixels of margin on all sides of the box. This means that there will be 10 pixels of space between the border of this element and any other elements on the page.

// By understanding the box model in CSS, you can more easily control the layout and spacing of elements on your web page. You can use the padding and margin properties to create space between elements, and the border property to create visual distinctions between elements.