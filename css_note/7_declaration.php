<?php
// A declaration in CSS is a combination of a property and a value that is applied to an HTML element to style it in a certain way. Here is the basic syntax for a CSS declaration:


// property: value;
// The property is the name of the CSS property that you want to set, such as color or font-size. The value is the value that you want to assign to that property, such as red or 16px.

// Here's an example of a declaration that sets the font color of a heading element to red:


// h1 {
//   color: red;
// }
// In this example, "h1" is the selector that targets the heading element, and "color: red;" is the declaration that sets the font color property to red.

// You can include multiple declarations within a CSS rule, each separated by a semicolon. Here's an example of a CSS rule with multiple declarations:


// h1 {
//   color: red;
//   font-size: 24px;
//   text-align: center;
// }
// In this example, the h1 element will have red font color, a font size of 24 pixels, and be centered within its containing element.

// You can also use shorthand properties to set multiple related values in a single declaration. For example, instead of setting separate properties for padding-top, padding-right, padding-bottom, and padding-left, you can use the shorthand "padding" property to set all four values at once:


// div {
//   padding: 10px 20px;
// }
// In this example, the div element will have 10 pixels of padding on the top and bottom, and 20 pixels of padding on the left and right.

// That's a brief introduction to declarations in CSS. I hope this helps you get started with styling your HTML elements with CSS!