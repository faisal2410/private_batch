// Border: The border property sets the width, style, and color of an element's border. For example, to set a solid 1px black border on a <div> element, you can do:


// div {
//     border: 1px solid black;
// }
// Border radius: The border - radius property sets the rounded corners of an element's border. For example, to set a 10px border radius on a <div> element, you can do:


// div {
//     border - radius: 10px;
// }
// Margin: The margin property sets the space outside an element's border. For example, to set a 20px margin on all sides of a <div> element, you can do:


// div {
//     margin: 20px;
// }
// Different ways to set margin: There are different ways to set the margin of an element.Here are some examples:

// Set margin on all sides: You can set the same margin value on all sides of an element by using the margin property.


// div {
//     margin: 20px;
// }

// Set margin on top / bottom and left / right separately: You can set the margin on top and bottom separately from the margin on left and right by using the margin - top, margin - bottom, margin - left, and margin - right properties.


// div {
//     margin - top: 10px;
//     margin - bottom: 20px;
//     margin - left: 30px;
//     margin - right: 40px;
// }

// Set margin using shorthand notation: You can use shorthand notation to set the margin on all sides and / or top / bottom and left / right separately.


// div {
//     margin: 10px 20px; /* top/bottom margin = 10px, left/right margin = 20px */
// }


// div {
//     margin: 10px 20px 30px 40px; /* top margin = 10px, right margin = 20px, bottom margin = 30px, left margin = 40px */
// }