// Padding is the space between the content of an HTML element and its border.It is used to increase the distance between the content and the border of an element.In CSS, padding can be set using the padding property.

// There are different ways to set padding in CSS:

// Set padding on all sides equally:


// padding: 10px;
// This will set the padding to 10 pixels on all sides of the element.

// Set padding on each side separately:


// padding - top: 10px;
// padding - right: 20px;
// padding - bottom: 10px;
// padding - left: 20px;
// This will set the padding to 10 pixels at the top and bottom and 20 pixels on the right and left sides of the element.

// Set padding with shorthand notation:


// padding: 10px 20px;
// This will set the padding to 10 pixels at the top and bottom and 20 pixels on the right and left sides of the element.

// The CSS box model is a design concept that explains how elements on a web page are rendered.It consists of four parts: content, padding, border, and margin.

//Content: This is the area where the content of the HTML element is displayed.

//Padding: This is the space between the content and the border.

//Border: This is the line around the content and padding.

//Margin: This is the space between the border and the next element on the page.

// Understanding the CSS box model is important for creating web layouts, as it helps in positioning and sizing of elements.