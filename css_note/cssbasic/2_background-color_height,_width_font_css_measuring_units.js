// some commonly used CSS properties with examples:

// background - color: It sets the background color of an element.The value can be any named color, hex code, or RGB value.For example:


// body {
//     background - color: #f1f1f1;
// }
// height: It sets the height of an element.The value can be in pixels, percentages, or any other CSS units.For example:


// div {
//     height: 100px;
// }
// width: It sets the width of an element.The value can be in pixels, percentages, or any other CSS units.For example:


// img {
//     width: 50 %;
// }
// font: It sets the font properties of an element, such as font - family, font - size, font - style, font - weight, etc.For example:


// p {
//     font - family: Arial;
//     font - size: 16px;
//     font - weight: bold;
// }
// CSS measuring units: There are various CSS units that can be used to specify values, such as pixels (px), percentages(%), ems(em), rems(rem), viewport width / height(vw / vh), etc.For example:


// div {
//     margin - top: 20px;
//     padding: 1em;
//     font - size: 2rem;
//     width: 80vw;
// }