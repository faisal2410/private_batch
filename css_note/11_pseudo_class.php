<?php
// In CSS, pseudo-classes and pseudo-elements are used to select and style elements based on their state or position within the document.

// Pseudo-classes are used to select elements based on their state, such as when they are hovered over, focused, or in a particular position in the document. Pseudo-elements are used to style parts of an element, such as the first letter or line of text.

// Here are examples of how to use pseudo-classes and pseudo-elements:

// Pseudo-class example:

// a:hover {
//   color: red;
// }
// This code selects any a element that is being hovered over and applies the color: red style to it.

// Pseudo-element example:


// p::first-letter {
//   font-size: 2em;
//   font-weight: bold;
// }
// This code selects the first letter of any p element and applies the font-size: 2em and font-weight: bold styles to it.

// Pseudo-classes:

// :hover - Selects an element when the user hovers over it with their mouse.

// :active - Selects an element when it is being clicked on or activated.

// :focus - Selects an element when it has focus, such as when it has been tabbed to or clicked on.

// :first-child - Selects the first child element of its parent.

// :last-child - Selects the last child element of its parent.

// :nth-child() - Selects an element based on its position in the parent, using a formula. For example, :nth-child(2n) selects every other child element.

// Pseudo-elements:

// ::before - Inserts content before the selected element.

// ::after - Inserts content after the selected element.

// ::first-line - Selects the first line of text in the selected element.

// ::first-letter - Selects the first letter of text in the selected element.

// ::selection - Selects the portion of the element that the user has selected.

