// In CSS, a rule consists of a selector and one or more declarations.

// The selector specifies which HTML elements on the page the rule should apply to. For example, if you want to apply a particular style to all paragraph elements on the page, you would use the "p" selector. You can also use more specific selectors to target specific elements on the page based on their attributes, classes, or IDs.

// The declarations within a rule specify the styles that should be applied to the selected elements. A declaration consists of a CSS property and a value, separated by a colon. For example, the following rule sets the color of all paragraphs on the page to red:


// p {
//   color: red;
// }
// In this example, "p" is the selector that targets all paragraph elements, and "color: red;" is the declaration that sets the font color property to red.

// You can include multiple declarations within a single rule, each separated by a semicolon. Here's an example of a rule with multiple declarations:


// h1 {
//   color: red;
//   font-size: 24px;
//   text-align: center;
// }
// In this example, the h1 element will have red font color, a font size of 24 pixels, and be centered within its containing element.

// By using rules to apply styles to HTML elements, you can create beautiful and dynamic web designs. You can also use CSS to apply styles to multiple elements at once, or to create responsive designs that adapt to different screen sizes and device types.




