<?php
// The <video> tag in HTML is used to embed a video file on a webpage. Similarly, the <audio> tag is used to embed an audio file on a webpage. Let's take a look at their syntax and some of their related attributes:

// Video Tag
// Syntax:


// <video src="video.mp4" width="640" height="360" controls></video>


// Attributes:

// src: This attribute specifies the source file URL of the video.
// width: This attribute specifies the width of the video in pixels.
// height: This attribute specifies the height of the video in pixels.
// controls: This attribute displays the video player controls (play, pause, volume, etc.).
// autoplay: This attribute automatically starts playing the video when the page is loaded.
// loop: This attribute loops the video playback.
// muted: This attribute mutes the video sound.
// Audio Tag
// Syntax:


// <audio src="audio.mp3" controls></audio>

// Attributes:

// src: This attribute specifies the source file URL of the audio.
// controls: This attribute displays the audio player controls (play, pause, volume, etc.).
// autoplay: This attribute automatically starts playing the audio when the page is loaded.
// loop: This attribute loops the audio playback.
// muted: This attribute mutes the audio sound.