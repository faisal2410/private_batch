/*
HTML is the standard markup language used to create web pages. It is composed of a series of tags that define the structure and content of a web page. Here are some basic tags to get you started:

HTML Document Structure:
The first thing you need to know about HTML is the basic structure of an HTML document. An HTML document starts with the <!DOCTYPE html> declaration and is followed by the <html> tag. The <html> tag contains two sections: the head section and the body section. The head section contains information about the document, such as the page title and links to CSS stylesheets, while the body section contains the visible content of the web page.
Example:


<!DOCTYPE html>
<html>
  <head>
    <title>My First Web Page</title>
  </head>
  <body>
    <h1>Hello, World!</h1>
    <p>Welcome to my website.</p>
  </body>
</html>
Headings:
Headings are used to create titles and sub-titles for sections of a web page. There are six levels of headings in HTML, from <h1> (the largest) to <h6> (the smallest).

Example:


<h1>Main Heading</h1>
<h2>Sub-Heading</h2>
Paragraphs:
Paragraphs are used to create blocks of text on a web page.
Example:


<p>This is a paragraph.</p>
Links:
Links are used to create clickable links to other web pages or to other sections of the same page.
Example:


<a href="https://www.example.com/">Link Text</a>
Images:
Images are used to display graphics on a web page.
Example:


<img src="image.jpg" alt="Image Description">
Lists:
Lists are used to create ordered or unordered lists.
Example:


<ul>
  <li>Item 1</li>
  <li>Item 2</li>
</ul>
<ol>
  <li>Item 1</li>
  <li>Item 2</li>
</ol>


These are just a few basic examples of HTML tags. As you learn more about HTML, you can use more advanced tags to create more complex and dynamic web pages.

*/ 