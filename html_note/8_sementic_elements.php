// Semantic elements are HTML elements that provide meaning and context to the content of a webpage, rather than just indicating how the content should be displayed. They help search engines and screen readers better understand the content of a webpage, which can improve accessibility and search engine optimization. Here are some examples of semantic elements in HTML:

// <header> element
// The <header> element represents a container for introductory content or a set of navigational links. It typically appears at the top of a webpage.

// Example:


// <header>
//    <h1>My Website</h1>
//    <nav>
//       <ul>
//          <li><a href="#">Home</a></li>
//          <li><a href="#">About</a></li>
//          <li><a href="#">Contact</a></li>
//       </ul>
//    </nav>
// </header>


// <nav> element
// The <nav> element represents a container for a set of navigational links.

// Example:


// <nav>
//    <ul>
//       <li><a href="#">Home</a></li>
//       <li><a href="#">About</a></li>
//       <li><a href="#">Contact</a></li>
//    </ul>
// </nav>
// <section> element
// The <section> element represents a section of a webpage, such as a chapter or a topic.

// Example:


// <section>
//    <h2>Chapter 1</h2>
//    <p>Lorem ipsum dolor sit amet, consectetur adipiscing elit. Donec eget sapien vitae mi elementum porttitor.</p>
// </section>
// <article> element
// The <article> element represents a self-contained piece of content, such as a blog post or a news article.

// Example:


// <article>
//    <h2>How to Make Pizza</h2>
//    <p>Lorem ipsum dolor sit amet, consectetur adipiscing elit. Donec eget sapien vitae mi elementum porttitor.</p>
// </article>
// <footer> element
// The <footer> element represents a container for footer content, such as copyright information or contact details.

// Example:


// <footer>
//    <p>&copy; 2023 My Website. All rights reserved.</p>
//    <p>Contact: contact@mywebsite.com</p>
// </footer>